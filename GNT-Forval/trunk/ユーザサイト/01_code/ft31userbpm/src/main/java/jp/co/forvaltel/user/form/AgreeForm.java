package jp.co.forvaltel.user.form;

import org.seasar.struts.annotation.Required;

import jp.co.forvaltel.common.action.AbstractAction;

/**
 * @author hungtp
 *
 */
public class AgreeForm extends AbstractAction {

	@Required
	private String token;
	
	@Required
	private String bpmId;
	
	private String moshikomiNo;
	private String webapiMsg;
	
	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}
	/**
	 * @param token the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}
	/**
	 * @return the bpmId
	 */
	public String getBpmId() {
		return bpmId;
	}
	/**
	 * @param bpmId the bpmId to set
	 */
	public void setBpmId(String bpmId) {
		this.bpmId = bpmId;
	}
	/**
	 * @return the moshikomiNo
	 */
	public String getMoshikomiNo() {
		return moshikomiNo;
	}
	/**
	 * @param moshikomiNo the moshikomiNo to set
	 */
	public void setMoshikomiNo(String moshikomiNo) {
		this.moshikomiNo = moshikomiNo;
	}
	/**
	 * @return the webapiMsg
	 */
	public String getWebapiMsg() {
		return webapiMsg;
	}
	/**
	 * @param webapiMsg the webapiMsg to set
	 */
	public void setWebapiMsg(String webapiMsg) {
		this.webapiMsg = webapiMsg;
	}

}
